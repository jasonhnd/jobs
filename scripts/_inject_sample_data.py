def main() -> int:
    for occ_id, fields in SAMPLES.items():
        path = DETAIL / f"{occ_id:04d}.json"
        if not path.exists():
            print(f"  [MISSING] {path} not found, skipping")
            continue
        data = json.loads(path.read_text(encoding="utf-8"))
        ai = data.setdefault("ai_risk", {})
        ai["rationale_long_ja"] = fields["rationale_long_ja"]
        ai["displaceable_tasks_ja"] = fields["displaceable_tasks_ja"]
        ai["resilient_tasks_ja"] = fields["resilient_tasks_ja"]
        ai["horizon_5y_ja"] = fields["horizon_5y_ja"]
        ai["rationale_generated_at"] = "2026-05-07"
        ai["rationale_model"] = "claude-opus-4-7"
        # Preserve compact one-line format consistent with existing detail files.
        path.write_text(
            json.dumps(data, ensure_ascii=False, separators=(",", ":")) + "\n",
            encoding="utf-8",
        )
        long_len = len(fields["rationale_long_ja"])
        horizon_len = len(fields["horizon_5y_ja"])
        title = data["title"]["ja"]
        print(
            f"  [OK] id={occ_id:>3} {title:<10} "
            f"rationale_long={long_len:>3} horizon_5y={horizon_len:>3}"
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
